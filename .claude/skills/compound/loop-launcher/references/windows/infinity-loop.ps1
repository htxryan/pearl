# Observability
# ============================================================
$STATUS_FILE = Join-Path $LOG_DIR '.loop-status.json'
$EXEC_LOG = Join-Path $LOG_DIR 'loop-execution.jsonl'

function Write-LoopStatus {
    param([string]$Status, [string]$EpicId = '', [int]$Attempt = 0)
    if ($Status -eq 'idle') {
        @{ status = 'idle'; timestamp = (Get-IsoTimestamp) } | ConvertTo-Json -Compress | Set-Content $STATUS_FILE -Encoding utf8
    } else {
        @{ epic_id = $EpicId; attempt = $Attempt; started_at = (Get-IsoTimestamp); status = $Status } |
            ConvertTo-Json -Compress | Set-Content $STATUS_FILE -Encoding utf8
    }
}

function Write-ResultLog {
    param([string]$EpicId, [string]$Result, [int]$Attempts, [long]$Duration)
    @{ epic_id = $EpicId; result = $Result; attempts = $Attempts; duration_s = $Duration; timestamp = (Get-IsoTimestamp) } |
        ConvertTo-Json -Compress | Add-Content $EXEC_LOG -Encoding utf8
}

# ============================================================
# Main Loop
# ============================================================
$COMPLETED = 0
$FAILED_COUNT = 0
$SKIPPED = 0
$script:Processed = @()
$LOOP_START = Get-EpochSeconds

Log "=========================================="
Log "Infinity loop starting"
Log "=========================================="
Log "Config: max_retries=$MAX_RETRIES model=$MODEL"
if (-not [string]::IsNullOrEmpty($EPIC_IDS)) { Log "Targeting epics: $EPIC_IDS" }
else { Log "Targeting: all ready epics" }

try {
    while ($true) {
        # Memory safety: clean up orphans and check memory
        Invoke-OrphanCleanup
        if (-not (Test-MemoryOk)) {
            Log "FATAL: Memory pressure too high, stopping loop to prevent system freeze"
            Log "  Hint: set MIN_FREE_MEMORY_PCT (current: ${MIN_FREE_MEMORY_PCT}%) or kill background processes"
            $FAILED_COUNT++
            break
        }

        $EPIC_ID = Get-NextEpic
        if ($null -eq $EPIC_ID) { break }

        Log "=========================================="
        Log "Processing epic: $EPIC_ID"
        Log "=========================================="
        $EPIC_START = Get-EpochSeconds

        $ATTEMPT = 0
        $RESULT = 'pending'

        Write-LoopStatus 'running' $EPIC_ID 1

        while ($ATTEMPT -le $MAX_RETRIES) {
            $ATTEMPT++
            $TS = Get-Timestamp
            $LOGFILE = Join-Path $LOG_DIR "loop_${EPIC_ID}-${TS}.log"
            $TRACEFILE = Join-Path $LOG_DIR "trace_${EPIC_ID}-${TS}.jsonl"

            Write-LoopStatus 'running' $EPIC_ID $ATTEMPT

            # [WINDOWS GAP] Symlink update for .latest
            # Requires Developer Mode. Falls back to writing the filename to a text file.
            $latestPath = Join-Path $LOG_DIR '.latest'
            try {
                if (Test-Path $latestPath) { Remove-Item $latestPath -Force }
                New-Item -ItemType SymbolicLink -Path $latestPath -Target (Split-Path $TRACEFILE -Leaf) -ErrorAction Stop | Out-Null
            } catch {
                # Fallback: hard link (no Developer Mode needed, same volume only)
                if (Test-Path $latestPath) { Remove-Item $latestPath -Force }
                try {
                    New-Item -ItemType HardLink -Path $latestPath -Target $TRACEFILE -ErrorAction Stop | Out-Null
                } catch {
                    # Last resort: plain text file with filename
                    Split-Path $TRACEFILE -Leaf | Set-Content $latestPath -Encoding utf8
                }
            }

            Log "Attempt $ATTEMPT/$($MAX_RETRIES + 1) for $EPIC_ID (log: $LOGFILE)"

            # Clean stale phase state (non-critical, may fail)
            try { ca phase-check clean 2>$null } catch { }

            if ($env:LOOP_DRY_RUN) {
                Log "[DRY RUN] Would run claude session for $EPIC_ID"
                $RESULT = 'complete'
                break
            }

            $PROMPT = Build-Prompt $EPIC_ID
            $MEM_LOG = Join-Path $LOG_DIR "memory_${EPIC_ID}-${TS}.log"
            $STDERR_FILE = "${LOGFILE}.stderr"

            # Run Claude session as a background process for watchdog monitoring.
            # Prompt is written to a temp file and piped via stdin (-p -) to avoid
            # command-line length limits (32KiB on Windows) and special char corruption.
            # Using -NoNewWindow (not -WindowStyle Hidden) so the child inherits
            # environment variables (ANTHROPIC_API_KEY, etc.).
            $promptFile = Join-Path $LOG_DIR "prompt_${EPIC_ID}-${TS}.txt"
            $PROMPT | Set-Content -Path $promptFile -NoNewline -Encoding utf8

            # Note: if 'claude' resolves to a .cmd wrapper (npm install), Start-Process
            # may not forward stdin correctly. Workaround: install Claude Code natively
            # or use WSL2 path instead.
            $proc = Start-Process -FilePath "claude" `
                -ArgumentList '--dangerously-skip-permissions','--permission-mode','auto','--model',$MODEL,'--output-format','stream-json','--verbose','-p','-' `
                -NoNewWindow -PassThru `
                -RedirectStandardInput $promptFile `
                -RedirectStandardOutput $TRACEFILE `
                -RedirectStandardError $STDERR_FILE

            Start-MemoryWatchdog $proc.Id $MEM_LOG
            Start-StaleWatchdog $proc.Id $TRACEFILE $MEM_LOG
            $proc.WaitForExit()
            Stop-StaleWatchdog
            Stop-MemoryWatchdog

            # Clean up prompt temp file
            Remove-Item $promptFile -Force -ErrorAction SilentlyContinue

            # Detect if watchdog killed the session
            if ((Test-Path $MEM_LOG) -and (Select-String -Path $MEM_LOG -Pattern 'STALE_WATCHDOG:' -Quiet)) {
                Log "WARN: Session killed by stale output watchdog (see $MEM_LOG)"
                Invoke-OrphanCleanup
            } elseif ((Test-Path $MEM_LOG) -and (Select-String -Path $MEM_LOG -Pattern 'WATCHDOG:' -Quiet)) {
                Log "WARN: Session killed by memory watchdog (see $MEM_LOG)"
                Invoke-OrphanCleanup
            }

            # Extract text from trace (post-hoc since we can't tee + pipe on Windows easily)
            Invoke-ExtractText $TRACEFILE $LOGFILE

            # Append stderr to macro log
            if ((Test-Path $STDERR_FILE) -and ((Get-Item $STDERR_FILE).Length -gt 0)) {
                Get-Content $STDERR_FILE | Add-Content $LOGFILE
                Remove-Item $STDERR_FILE -Force
            }

            # Health check: warn if macro log extraction failed
            if ((Test-Path $TRACEFILE) -and ((Get-Item $TRACEFILE).Length -gt 0) -and
                (-not (Test-Path $LOGFILE) -or (Get-Item $LOGFILE).Length -eq 0)) {
                Log "WARN: Macro log is empty but trace has content (extract_text may have failed)"
            }

            $MARKER = Find-Marker $LOGFILE $TRACEFILE

            switch -Wildcard ($MARKER) {
                'complete' {
                    Log "Epic $EPIC_ID completed successfully"
                    $RESULT = 'complete'
                }
                'human:*' {
                    $reason = $MARKER -replace '^human:', ''
                    Log "Epic $EPIC_ID needs human action: $reason"
                    try { bd update $EPIC_ID --notes "Human required: $reason" 2>$null } catch { }
                    $RESULT = 'skip'
                }
                'failed' {
                    Log "Epic $EPIC_ID reported failure (attempt $ATTEMPT)"
                }
                default {
                    Log "Epic $EPIC_ID session ended without marker (attempt $ATTEMPT)"
                }
            }

            # break exits switch only, not while -- check result explicitly
            if ($RESULT -ne 'pending') { break }

            if ($ATTEMPT -le $MAX_RETRIES) {
                Log "Retrying $EPIC_ID..."
                Invoke-OrphanCleanup
                Start-Sleep -Seconds 5
            }
        }

        $EPIC_DURATION = (Get-EpochSeconds) - $EPIC_START

        if ($RESULT -eq 'complete') {
            if (-not $env:LOOP_DRY_RUN) {
                # Verify working tree is clean after epic completion
                $dirty = $false
                try { git diff --quiet 2>$null } catch { $dirty = $true }
                try { git diff --cached --quiet 2>$null } catch { $dirty = $true }
                if ($dirty) {
                    Log "WARN: Working tree dirty after epic completion, auto-committing"
                    try { git add -A 2>$null } catch { }
                    try { git commit -m "chore: auto-commit uncommitted changes from $EPIC_ID" 2>$null } catch { }
                }
                $COMPLETED++
                Write-ResultLog $EPIC_ID 'complete' $ATTEMPT $EPIC_DURATION
                Log "Epic $EPIC_ID done. Completed so far: $COMPLETED"
            }
        } elseif ($RESULT -eq 'skip') {
            $SKIPPED++
            Write-ResultLog $EPIC_ID 'skipped' $ATTEMPT $EPIC_DURATION
            Log "Epic $EPIC_ID skipped (human required). Continuing."
        } elseif ($RESULT -eq 'pending') {
            $FAILED_COUNT++
            Write-ResultLog $EPIC_ID 'failed' $ATTEMPT $EPIC_DURATION
            Log "Epic $EPIC_ID failed after $($MAX_RETRIES + 1) attempts. Stopping loop."
            $script:Processed += $EPIC_ID
            break
        }

        $script:Processed += $EPIC_ID
    }
} catch {
    # Crash handler -- catches terminating errors (strict mode, native command failures)
    Log "CRASH: $_"
    @{ status = 'crashed'; error = "$_"; timestamp = (Get-IsoTimestamp) } |
        ConvertTo-Json -Compress | Set-Content $STATUS_FILE -ErrorAction SilentlyContinue
} finally {
    Stop-MemoryWatchdog
    Stop-StaleWatchdog
}

# ============================================================
# Post Loop
# ============================================================
$TOTAL_DURATION = (Get-EpochSeconds) - $LOOP_START

if (-not $env:LOOP_DRY_RUN) {
    @{ type = 'summary'; completed = $COMPLETED; failed = $FAILED_COUNT; skipped = $SKIPPED; total_duration_s = $TOTAL_DURATION } |
        ConvertTo-Json -Compress | Add-Content $EXEC_LOG -Encoding utf8
    Write-LoopStatus 'idle'

    # Push to remote if available
    try {
        $null = git remote get-url origin 2>$null
        Log "Pushing to remote..."
        git push 2>&1
    } catch {
        Log "WARN: git push failed: $_"
    }
}

Log "=========================================="
Log "Loop finished"
Log "  Completed: $COMPLETED"
Log "  Failed:    $FAILED_COUNT"
Log "  Skipped:   $SKIPPED"
$minutes = [math]::Floor($TOTAL_DURATION / 60)
Log "  Duration:  ${TOTAL_DURATION}s (${minutes}m)"
Log "  Processed: $($script:Processed -join ' ')"
Log "=========================================="

# Exit code
if ($COMPLETED -eq 0 -and $FAILED_COUNT -eq 0) {
    Log "WARN: Zero epics completed -- all may be blocked or skipped"
