# [WINDOWS GAP] cleanup_orphans -- Cannot inspect process CWD on Windows.
# The bash version uses lsof/readlink to scope kills to the current repo.
# On Windows, we skip CWD filtering and match against the full command line
# (like pgrep -f) using WMI, which is broader but the best available.
function Invoke-OrphanCleanup {
    $killed = 0
    $patterns = @('vitest', 'go\.test', 'pytest', 'cargo\.test', 'node.*\.test\.')
    try {
        $procs = Get-CimInstance Win32_Process -ErrorAction SilentlyContinue
        foreach ($proc in $procs) {
            if ([string]::IsNullOrEmpty($proc.CommandLine)) { continue }
            foreach ($pattern in $patterns) {
                if ($proc.CommandLine -match $pattern) {
                    # [WINDOWS GAP] No CWD check -- kills ALL matching processes, not just repo-scoped ones
                    Stop-Process -Id $proc.ProcessId -Force -ErrorAction SilentlyContinue
                    $killed++
                    break  # Don't double-count across patterns
                }
            }
        }
    } catch { }
    if ($killed -gt 0) {
        Log "Cleaned up $killed orphan test processes"
        Start-Sleep -Seconds 2
    }
}

# get_memory_pct equivalent -- uses WMI via Get-CimInstance
function Get-FreeMemoryPct {
    try {
        $os = Get-CimInstance -ClassName Win32_OperatingSystem
        if ($os.TotalVisibleMemorySize -eq 0) { return $null }
        return [math]::Round(($os.FreePhysicalMemory / $os.TotalVisibleMemorySize) * 100)
    } catch {
        return $null
    }
}

# check_memory equivalent
function Test-MemoryOk {
    $freePct = Get-FreeMemoryPct
    if ($null -eq $freePct) { return $true }  # Can't measure, assume OK
    if ($freePct -lt $MIN_FREE_MEMORY_PCT) {
        Log "WARN: System memory ${freePct}% free (minimum: ${MIN_FREE_MEMORY_PCT}%)"
        return $false
    }
    Log "Memory OK: ${freePct}% free"
    return $true
}

# Memory watchdog -- background job that monitors memory and kills target PID
# [WINDOWS GAP] Stop-Process does NOT kill the process tree. Only the target PID.
$script:WatchdogJob = $null

function Start-MemoryWatchdog {
    param([int]$TargetPid, [string]$MemLog)
    $script:WatchdogJob = Start-Job -ScriptBlock {
        param($pid, $log, $thresh, $interval)
        while ($null -ne (Get-Process -Id $pid -ErrorAction SilentlyContinue)) {
            try {
                $os = Get-CimInstance -ClassName Win32_OperatingSystem
                $pct = [math]::Round(($os.FreePhysicalMemory / $os.TotalVisibleMemorySize) * 100)
                "$(Get-Date -Format 'yyyy-MM-dd_HH-mm-ss') memory_free=${pct}%" | Add-Content $log
                if ($pct -lt $thresh) {
                    "$(Get-Date -Format 'yyyy-MM-dd_HH-mm-ss') WATCHDOG: memory ${pct}% < ${thresh}%, killing PID $pid" | Add-Content $log
                    # [WINDOWS GAP] Stop-Process does NOT kill the process tree
                    Stop-Process -Id $pid -Force -ErrorAction SilentlyContinue
                    break
                }
            } catch { }
            Start-Sleep -Seconds $interval
        }
    } -ArgumentList $TargetPid, $MemLog, $WATCHDOG_THRESHOLD, $WATCHDOG_INTERVAL
}

function Stop-MemoryWatchdog {
    if ($null -ne $script:WatchdogJob) {
        Stop-Job $script:WatchdogJob -ErrorAction SilentlyContinue
        Remove-Job $script:WatchdogJob -Force -ErrorAction SilentlyContinue
        $script:WatchdogJob = $null
    }
}

# Stale output watchdog -- kills session if trace file stops growing
$script:StaleWatchdogJob = $null

function Start-StaleWatchdog {
    param([int]$TargetPid, [string]$TraceFile, [string]$LogFile)
    $script:StaleWatchdogJob = Start-Job -ScriptBlock {
        param($pid, $trace, $log, $interval, $timeout)
        $lastSize = 0
        $staleSecs = 0
        while ($null -ne (Get-Process -Id $pid -ErrorAction SilentlyContinue)) {
            Start-Sleep -Seconds $interval
            $curSize = 0
            if (Test-Path $trace) { $curSize = (Get-Item $trace).Length }
            if (($curSize -eq $lastSize) -and ($lastSize -gt 0)) {
                $staleSecs += $interval
                if ($staleSecs -ge $timeout) {
                    "$(Get-Date -Format 'yyyy-MM-dd_HH-mm-ss') STALE_WATCHDOG: no output for ${staleSecs}s, killing PID $pid" | Add-Content $log
                    # [WINDOWS GAP] Stop-Process does NOT kill the process tree
                    Stop-Process -Id $pid -Force -ErrorAction SilentlyContinue
                    break
                }
            } else {
                $staleSecs = 0
            }
            $lastSize = $curSize
        }
    } -ArgumentList $TargetPid, $TraceFile, $LogFile, $WATCHDOG_INTERVAL, $SESSION_STALE_TIMEOUT
}

function Stop-StaleWatchdog {
    if ($null -ne $script:StaleWatchdogJob) {
        Stop-Job $script:StaleWatchdogJob -ErrorAction SilentlyContinue
        Remove-Job $script:StaleWatchdogJob -Force -ErrorAction SilentlyContinue
        $script:StaleWatchdogJob = $null
    }
}

# ============================================================
# JSON Parsing (equivalent to parse_json with jq/python3 fallback)
# ============================================================
# PowerShell's ConvertFrom-Json replaces jq entirely.
function Invoke-ParseJson {
    param(
        [Parameter(ValueFromPipeline)][string]$JsonInput,
        [string]$Filter
    )
    $data = $JsonInput | ConvertFrom-Json
    if ($data -is [array]) { $data = $data[0] }
    $parts = $Filter.TrimStart('.') -split '\.'
    $v = $data
    foreach ($p in $parts) {
        if ([string]::IsNullOrEmpty($p)) { continue }
        $v = $v.$p
    }
    return $v
}

# ============================================================
# Epic Selector
# ============================================================
function Test-DepsClosed {
    param([string]$EpicId)
    try {
        $depsJson = bd show $EpicId --json 2>$null
        if ([string]::IsNullOrEmpty($depsJson)) { return $true }
        $data = $depsJson | ConvertFrom-Json
        if ($data -is [array]) { $data = $data[0] }
        $deps = if ($null -ne $data.depends_on) { $data.depends_on }
                elseif ($null -ne $data.dependencies) { $data.dependencies }
                else { @() }
        foreach ($d in $deps) {
            $status = if ($d -is [string]) { 'open' } else { $d.status }
            if ($status -ne 'closed') {
                $blockId = if ($d -is [string]) { $d } else { $d.id }
                Log "Skip ${EpicId}: blocked by dependency $blockId (not closed)"
                return $false
            }
        }
        return $true
    } catch {
        return $true
    }
}

function Get-NextEpic {
    if (-not [string]::IsNullOrEmpty($EPIC_IDS)) {
        foreach ($epicId in ($EPIC_IDS -split '\s+')) {
            if ($script:Processed -contains $epicId) { continue }
            try {
                $json = bd show $epicId --json 2>$null
                $status = $json | Invoke-ParseJson -Filter '.status'
                if ($status -eq 'open') {
                    if (Test-DepsClosed $epicId) { return $epicId }
                }
            } catch { continue }
        }
        return $null
    } else {
        try {
            $json = bd list --type=epic --ready --json --limit=10 2>$null
            $items = $json | ConvertFrom-Json
            foreach ($item in $items) {
                if ($script:Processed -contains $item.id) { continue }
                if (Test-DepsClosed $item.id) { return $item.id }
            }
        } catch { }
        return $null
    }
}

