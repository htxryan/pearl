function Build-Prompt {
    param([string]$EpicId)
    # Six backticks (``````): here-strings pass backticks literally. Six backticks form
    # a valid 6-backtick markdown code fence, avoiding conflict with triple-backtick content.
    return @"
You are running in an autonomous infinity loop. Your task is to fully implement a beads epic.

## Step 1: Load context
Run these commands to prime your session:

``````bash
npx ca load-session
bd show $EpicId
``````

Read the epic details carefully. Understand scope, acceptance criteria, and sub-tasks.

## Step 2: Execute the workflow
Run the full compound workflow for this epic, starting from the plan phase
(spec-dev is already done -- the epic exists):

/compound:cook-it from plan -- Epic: $EpicId

Work through all phases: plan, work, review, compound.

## Step 3: On completion
When all work is done and tests pass:
1. Close the epic: ``bd close $EpicId``
2. Commit and push all changes
3. Output this exact marker on its own line:

EPIC_COMPLETE

## Step 4: On failure
If you cannot complete the epic after reasonable effort:
1. Add a note: ``bd update $EpicId --notes "Loop failed: <reason>"``
2. Output this exact marker on its own line:

EPIC_FAILED

## Step 5: On human required
If you hit a blocker that REQUIRES human action (account creation, API keys,
external service setup, design decisions you cannot make, etc.):
1. Add a note: ``bd update $EpicId --notes "Human required: <reason>"``
2. Output this exact marker followed by a short reason on the SAME line:

HUMAN_REQUIRED: <reason>

Example: HUMAN_REQUIRED: Need AWS credentials configured in .env

## Memory Safety Rules
- For Go work, use ``go test ./...`` in the go/ directory.
- NEVER run embedding tests unless the epic modifies embedding code.
- Between test runs, wait for all child processes to exit before starting another.

## Rules
- Do NOT ask questions -- there is no human. Make reasonable decisions.
- Do NOT stop early -- complete the full workflow.
- If tests fail, fix them. Retry up to 3 times before declaring failure.
- Use HUMAN_REQUIRED only for true blockers that no amount of retrying can solve.
- Commit incrementally as you make progress.
"@
}

# ============================================================
# Text Extractor (equivalent to extract_text)
# ============================================================
# Extracts assistant text from Claude Code stream-json events.
# Reads a JSONL file and writes extracted text to an output file.
function Invoke-ExtractText {
    param([string]$TraceFile, [string]$OutputFile)
    if (-not (Test-Path $TraceFile)) { return }
    $results = [System.Collections.Generic.List[string]]::new()
    switch -File $TraceFile {
        default {
            $line = $_.Trim()
            if ([string]::IsNullOrEmpty($line)) { continue }
            try {
                $obj = $line | ConvertFrom-Json -ErrorAction SilentlyContinue
                if ($obj.type -eq 'assistant') {
                    foreach ($block in $obj.message.content) {
                        if ($block.type -eq 'text' -and -not [string]::IsNullOrEmpty($block.text)) {
                            $results.Add($block.text)
                        }
                    }
                }
            } catch { }
        }
    }
    $results | Set-Content $OutputFile -Encoding utf8
}

# ============================================================
# Marker Detection
# ============================================================
function Find-Marker {
    param([string]$LogFile, [string]$TraceFile)

    # Primary: check extracted text with anchored patterns
    if ((Test-Path $LogFile) -and ((Get-Item $LogFile).Length -gt 0)) {
        $content = Get-Content $LogFile -Raw
        if ($content -match '(?m)^EPIC_COMPLETE$') { return 'complete' }
        if ($content -match '(?m)^HUMAN_REQUIRED:\s*(.+)$') {
            return "human:$($Matches[1])"
        }
        if ($content -match '(?m)^EPIC_FAILED$') { return 'failed' }
    }

    # Fallback: check raw trace JSONL (unanchored)
    if ((Test-Path $TraceFile) -and ((Get-Item $TraceFile).Length -gt 0)) {
        $content = Get-Content $TraceFile -Raw
        if ($content -match 'EPIC_COMPLETE') { return 'complete' }
        if ($content -match 'HUMAN_REQUIRED:') { return 'human:detected in trace' }
        if ($content -match 'EPIC_FAILED') { return 'failed' }
    }

    return 'none'
}

