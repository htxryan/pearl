<#
.SYNOPSIS
  Infinity Loop - PowerShell reference template
  Structural translation of the bash infinity-loop.sh generated by: ca loop

.DESCRIPTION
  Autonomously processes beads epics via Claude Code sessions.
  This is a STATIC REFERENCE TEMPLATE -- not generated by ca loop.
  Copy and adapt for your project.

  Usage:
    pwsh -NonInteractive -File infinity-loop.ps1
    $env:LOOP_DRY_RUN = "1"; pwsh -NonInteractive -File infinity-loop.ps1  # Preview

.NOTES
  Known Limitations (Windows):
  - No screen/tmux equivalent: runs in foreground only.
    Use Start-Process for background execution (no attach/detach).
  - [WINDOWS GAP] No process group kill (kill -- -$PID).
    Stop-Process kills the target only, not its child tree.
  - [WINDOWS GAP] No CWD-scoped orphan cleanup.
    Cannot inspect a running process's working directory from PowerShell.
  - [WINDOWS GAP] Symlinks require Developer Mode on Windows 10/11.
    Falls back to file copy for .latest pointer.
  - Memory watchdog uses Get-CimInstance (WMI) -- slightly slower than /proc/meminfo.
  - [WINDOWS GAP] No review trigger system. The bash version supports --review-every N
    with inter-process signaling. This template runs epics sequentially without
    review triggers. Use the polish loop (via WSL2) for post-hoc review.
  - Claude Code on Windows runs tool commands in Git Bash internally,
    so prompts containing bash commands work regardless.
#>

